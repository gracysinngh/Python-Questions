# 19.Your task is to write a program that takes two input numbers a and b representing a fraction a/b (where a < b). The program should print all the reduced forms of the given fraction in a single line. If the input fraction is already in its most reduced form, print -1.


from math import gcd

def all_reduced_forms(a, b):
    forms = []
    for div in range(2, min(a, b)+1):
        if a % div == 0 and b % div == 0:
            forms.append(f"{a//div}/{b//div}")
    return forms

def solve(a, b):
    if gcd(a, b) == 1:
        print(-1)
        return
    results = all_reduced_forms(a, b)
    print(' '.join(results))


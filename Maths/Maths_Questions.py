# 4.Your task in this problem is to find the number of pentagonal numbers Pn (where n < N) such that the difference or sum of two pentagonal numbers Pn and Pn-K is also a pentagonal number.

# Pentagonal numbers are generated by the formula:

# Pn = n (3n − 1) / 2

import math


def is_pentagonal(x):
    if x <= 0:
        return False
    n = (1 + math.sqrt(1 + 24 * x)) / 6
    return n.is_integer()

# Input
N = int(input())
K = int(input())

# Generate all pentagonal numbers up to N
pent = [n * (3 * n - 1) // 2 for n in range(1, N + 1)]

count = 0

# Check for all pairs (Pn, Pn-K)
for n in range(K, N):
    Pn = pent[n - 1]
    Pk = pent[n - K - 1]

    diff = Pn - Pk
    summ = Pn + Pk

    # Check if either sum or difference is pentagonal
    if is_pentagonal(diff) or is_pentagonal(summ):
        count += 1

print(count)



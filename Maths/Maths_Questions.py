# MergeSort in Python

def mergeSort(array):
    if len(array) > 1:
        mid = len(array) // 2
        left = array[:mid]
        right = array[mid:]

        # Sort the two halves
        mergeSort(left)
        mergeSort(right)

        # Merge the sorted halves
        i = j = k = 0

        # Compare elements and merge
        while i < len(left) and j < len(right):
            if left[i] < right[j]:
                array[k] = left[i]
                i += 1
            else:
                array[k] = right[j]
                j += 1
            k += 1

        # If elements left in left array
        while i < len(left):
            array[k] = left[i]
            i += 1
            k += 1

        # If elements left in right array
        while j < len(right):
            array[k] = right[j]
            j += 1
            k += 1


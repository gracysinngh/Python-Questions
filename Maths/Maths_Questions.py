# Sort the array using insertion sort
insertionSort(array)

# Print the sorted array
print(*array)


# 23.Bubble Sort

def bubbleSort(arr):
    n = len(arr)
    for i in range(n):
        for j in range(0, n - i - 1):
            if arr[j]>arr[j + 1]:
                arr[j],arr[j+1] = arr[j+1],arr[j]
    return


